var myVid = document.getElementById("myVid");

myVid.addEventListener("mouseover", function() {
	this.setAttribute("src", "https://www.youtube.com/embed/k-j5a59L-E8?autoplay=1&mute=0");
});
myVid.addEventListener("mouseleave", function() {
	this.setAttribute("src", "https://www.youtube.com/embed/k-j5a59L-E8?autoplay=0&mute=0");
});


